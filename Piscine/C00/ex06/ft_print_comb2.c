#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			ft_putchar('0' + a / 10);
			ft_putchar('0' + a % 10);
			write(1, " ", 1);
			ft_putchar('0' + b / 10);
			ft_putchar('0' + b % 10);
			b++;
			if (a != 98)
				write(1, ", ", 2);
		}
		a++;
	}
}
